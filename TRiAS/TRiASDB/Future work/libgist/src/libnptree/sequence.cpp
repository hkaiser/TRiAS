void
Sequence::localAlign(
    const Sequence& s,
    const ScoringMatrix& m,
    int gapPenalty,
    double& score,
    double& prob)
{
    ScoringMatrix::init();

    static int count = 0;
    int* alignMatrix = new int[(strlen(seq) + 1) * (strlen(s.seq) + 1)];
    int slen = strlen(seq) + 1;

    int i, j;
    int maxScore = 0;
    for (i = 0; i <= strlen(seq); i++) {
        for (j = 0; j <= strlen(s.seq); j++) {
	    if (j == 0 || i == 0) {
	        alignMatrix[i * slen + j] = 0;
	    } else {
		int a = alignMatrix[(i-1) * slen + j-1] + m.score(seq[i-1], s.seq[j-1]);
		int b = alignMatrix[(i-1) * slen + j] - gapPenalty;
		int c = alignMatrix[i * slen + j-1] - gapPenalty;
	        alignMatrix[i * slen + j] = max(0, max(a, max(b, c)));
	    }
	    if (alignMatrix[i * slen + j] > maxScore) {
	        maxScore = alignMatrix[i * slen + j];
	    }

	    // debug
	    //cout << alignMatrix[i * slen + j] << " ";
	}

	// debug
	//cout << endl;
    }

    delete [] alignMatrix;
    score = maxScore;

    // debug
//    count++;
//    cout << count << endl;
}
